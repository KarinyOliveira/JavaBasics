
public class VarTest {
	
	public static void main(String[] args) {
		
		int iNum = 5;
		char cVal = 'S';
		boolean bVal = false;
		
		System.out.println("Integer: " + iNum);
		System.out.println("Char: " + cVal);
		System.out.println("Boolean: " + bVal);
		
		iNum = 25;
		cVal = 'B';
		bVal = true;
		
		System.out.println("------------ UPDATE VALUES ------------------");
		System.out.println("Integer: " + iNum);
		System.out.println("Char: " + cVal);
		System.out.println("Boolean: " + bVal);
	}

}
